/* create a two parameter nucleotide substitution matrix */
substitution_matrix_t* create_two_param_nuc_subs_matrix() {

	char* alphabet = "ATCG";

	static int matrix0[] = { 2, -1, -1, -1};
	static int matrix1[] = {-1,  2, -1, -1};
	static int matrix2[] = {-1, -1,  2, -1};
	static int matrix3[] = {-1, -1, -1,  2};
	int* matrix[] =  {matrix0, matrix1, matrix2, matrix3};

	return sm_create(alphabet, matrix);
}

